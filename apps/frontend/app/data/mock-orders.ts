const today = new Date()
const d = (offsetDays: number, hour = 10, minute = 0) => {
  const dt = new Date(today)
  dt.setDate(dt.getDate() - offsetDays)
  dt.setHours(hour, minute, 0, 0)
  return dt.toISOString()
}

